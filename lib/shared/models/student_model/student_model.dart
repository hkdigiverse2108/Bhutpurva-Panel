class StudentModel {
  final String id;
  final String image;
  final String name;
  final String address;
  final String phone;
  final String email;
  final int age;
  final String profileStatus;

  StudentModel({
    required this.id,
    required this.image,
    required this.name,
    required this.address,
    required this.phone,
    required this.email,
    required this.age,
    required this.profileStatus,
  });
}
